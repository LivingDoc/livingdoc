package org.livingdoc.example

class CalculatorInt {

    fun sum(a: Int, b: Int): Int {
        return a + b
    }

    fun diff(a: Int, b: Int): Int {
        return a - b
    }

    fun multiply(a: Int, b: Int): Int {
        return a * b
    }

    fun divide(a: Int, b: Int): Int {
        return a / b
    }
}
